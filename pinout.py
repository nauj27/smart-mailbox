class Pinout(object):
    D0 = 16 # GPIO16 - USER - WAKE
    D1 = 5  # GPIO5
    D2 = 4  # GPIO4
    D3 = 0  # GPIO0 - FLASH
    D4 = 2  # GPIO2 - TXD1
    D5 = 14 # GPIO14 - HSCLK
    D6 = 12 # GPIO12 - HMISO
    D7 = 13 # GPIO13 - RXD2 - HMOSI
    D8 = 15 # GPIO15 - TXD2 - HCS
    RX = 3  # GPIO3 - RXD0
    TX = 1  # GPIO1 - TXD0
    A0 = 0  # TOUT - ADC0
    S3 = 10 # GPIO10 - SDD3
    S2 = 9  # GPIO9 - SDD2
    S1 = 'MOSI' # SDD1
    SC = 'CS'   # SDCMD
    S0 = 'MISO' # SDD0
    SK = 'SCLK' # SDCLK
